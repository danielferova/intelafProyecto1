    private void llenarCombo() {
        
        Conexion conexion = new Conexion();
        ResultSet rs = conexion.getTabla("SELECT Nombre_Cliente, Nit_Cliente FROM CLIENTE");
        try {
            while (rs.next()) {
                nombreCliente.addItem(rs.getString(1));
                tablaTienda.add(rs.getString(2));
            }

        } catch (Exception e) {

        }
    }
    
